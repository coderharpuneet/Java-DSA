public class ASCIIvalues {
    public static void main(String[] args) {
        char ch='A';
        System.out.println(ch);
        System.out.printf("\n");
        char d=65;
        System.out.println(d);
        System.out.printf("\n");
        char oct=0101;
        System.out.println(oct);
        System.out.printf("\n");
        char hexa=0x41;
        System.out.println(hexa);
        System.out.printf("\n");
        char b=0b1000001;
        System.out.println(b);
        System.out.printf("\n");
        char uni='\u0041';
        System.out.println(uni);
        System.out.printf("\n");
    }
}
