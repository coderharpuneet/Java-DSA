public class arithmeticOperations {
    public static void main(String[] args) {
        int a=5;
        int b=2;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        double x=5.2;
        double y=3.6;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        
    }
}
